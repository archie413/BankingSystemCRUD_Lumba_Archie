    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
    
    if (selectedRow >= 0) {
        // Grab the Account ID from the first column of the selected row
        int accountId = (int) jTable1.getValueAt(selectedRow, 0);

        // Query the database to get all the customer and account details
        String query = "SELECT c.first_name, c.last_name, c.email, c.phone_number, a.account_type, a.balance " +
                       "FROM Account a JOIN Customer c ON a.customer_id = c.customer_id " +
                       "WHERE a.account_id = ?";
                       
        try (java.sql.Connection conn = DBConnection.getConnection(); 
             java.sql.PreparedStatement stmt = conn.prepareStatement(query)) {
             
            stmt.setInt(1, accountId);
            java.sql.ResultSet rs = stmt.executeQuery();
            
            // Populate the text fields with the retrieved data
            if (rs.next()) {
                txtFirstName.setText(rs.getString("first_name"));
                txtLastName.setText(rs.getString("last_name"));
                txtEmail.setText(rs.getString("email"));
                txtNumber.setText(rs.getString("phone_number"));
                ComboBox1.setSelectedItem(rs.getString("account_type"));
                txtBalance.setText(String.valueOf(rs.getDouble("balance")));
            }
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Error loading account details.");
        }
    }
    }//GEN-LAST:event_jTable1MouseClicked

