    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
    if (selectedRow < 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a row to delete!");
        return;
    }
    
    int selectedAccountId = (int) jTable1.getValueAt(selectedRow, 0);
    
    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this account?", "Confirm Delete", javax.swing.JOptionPane.YES_NO_OPTION);
    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        try (java.sql.Connection conn = DBConnection.getConnection(); 
             java.sql.PreparedStatement stmt = conn.prepareStatement("DELETE FROM Account WHERE account_id = ?")) {
             stmt.setInt(1, selectedAccountId);
             stmt.executeUpdate();
             javax.swing.JOptionPane.showMessageDialog(this, "Account deleted successfully!");
             RefreshActionPerformed(evt);
        } catch(Exception e) { e.printStackTrace(); }
    }
    }//GEN-LAST:event_DeleteActionPerformed

