    private void AccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AccountActionPerformed
        // TODO add your handling code here:
        String fName = txtFirstName.getText().trim();
    String lName = txtLastName.getText().trim();
    String email = txtEmail.getText().trim();
    String phone = txtNumber.getText().trim(); // Note: classmate used txtNumber
    String balanceText = txtBalance.getText().trim();
    String type = ComboBox1.getSelectedItem().toString().trim();

    if (fName.isEmpty() || lName.isEmpty() || email.isEmpty() || phone.isEmpty() || balanceText.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please fill out all fields!");
        return; 
    }
    
    try {
        double balance = Double.parseDouble(balanceText);
        int selectedRow = jTable1.getSelectedRow();
        int customerId = -1;

        if (selectedRow >= 0) {
            int selectedAccountId = (int) jTable1.getValueAt(selectedRow, 0); 
            try (java.sql.Connection conn = DBConnection.getConnection(); 
                 java.sql.PreparedStatement stmt = conn.prepareStatement("SELECT customer_id FROM Account WHERE account_id = ?")) {
                 stmt.setInt(1, selectedAccountId);
                 java.sql.ResultSet rs = stmt.executeQuery();
                 if(rs.next()) customerId = rs.getInt("customer_id");
            } catch(Exception e) { e.printStackTrace(); }
        }

        if (customerId != -1) {
            customerDAO.updateCustomer(customerId, fName, lName, email, phone);
            accountDAO.addAccount(customerId, type, balance);
            javax.swing.JOptionPane.showMessageDialog(this, "New Account added to existing Customer!");
        } else {
            customerId = customerDAO.addCustomer(fName, lName, email, phone);
            if (customerId != -1) {
                accountDAO.addAccount(customerId, type, balance);
                javax.swing.JOptionPane.showMessageDialog(this, "Customer and Account added successfully!");
            }
        }
        RefreshActionPerformed(evt); // Automatically refresh
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Balance must be a number!");
    }
    }//GEN-LAST:event_AccountActionPerformed

