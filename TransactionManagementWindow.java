    private void DepositActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DepositActionPerformed
        // TODO add your handling code here:
        // 1. Did they verify an account first?
    if (verifiedAccountId == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please verify an account first before depositing!", "Verification Required", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    String amountStr = txtAmout.getText().trim(); 
    
    if (amountStr.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter an amount to deposit.", "Missing Amount", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    try {
        double amount = Double.parseDouble(amountStr);
        if (amount <= 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Deposit amount must be greater than zero.", "Invalid Amount", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // 2. Process the Deposit using your DAO
        boolean success = transactionDAO.processTransaction(verifiedAccountId, amount, "Deposit");
        
        if (success) {
            javax.swing.JOptionPane.showMessageDialog(this, "Deposit Successful!");
            
           // 3. Automatically update the current balance box on the screen!
            currentAccountBalance += amount; // Add to the background variable
            txtCurrentBalance.setText("Owner: " + currentAccountOwner + "   |   Balance: ₱" + currentAccountBalance);
            
            // Clear the amount box for the next transaction
            txtAmout.setText(""); 
        }
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid number for the amount.", "Invalid Input", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_DepositActionPerformed

