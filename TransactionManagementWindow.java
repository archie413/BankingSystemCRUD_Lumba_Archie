    private void WithdrawActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_WithdrawActionPerformed
        // TODO add your handling code here:
        // 1. Did they verify an account first?
    if (verifiedAccountId == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please verify an account first before withdrawing!", "Verification Required", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    String amountStr = txtAmout.getText().trim();
    
    if (amountStr.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter an amount to withdraw.", "Missing Amount", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    try {
        double amount = Double.parseDouble(amountStr);
        if (amount <= 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Withdrawal amount must be greater than zero.", "Invalid Amount", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // 2. Process the Withdrawal
        // (Your TransactionDAO will automatically check if they have enough balance!)
        boolean success = transactionDAO.processTransaction(verifiedAccountId, amount, "Withdraw");
        
        if (success) {
            javax.swing.JOptionPane.showMessageDialog(this, "Withdrawal Successful!");
            
          // 3. Automatically update the current balance box on the screen!
            currentAccountBalance -= amount; // Subtract from the background variable
            txtCurrentBalance.setText("Owner: " + currentAccountOwner + "   |   Balance: ₱" + currentAccountBalance);
            
            txtAmout.setText("");
        }
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid number for the amount.", "Invalid Input", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_WithdrawActionPerformed

    private void VerificationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VerificationActionPerformed
        // TODO add your handling code here:
      String searchId = txtSearchBar.getText().trim();
    
        if (searchId.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter an Account ID to verify.", "Missing ID", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try {
            int accId = Integer.parseInt(searchId);
            
            // Updated query to JOIN Customer and Account to get the owner's name
            String query = "SELECT a.balance, c.first_name, c.last_name FROM Account a JOIN Customer c ON a.customer_id = c.customer_id WHERE a.account_id = ?";
            
            try (java.sql.Connection conn = DBConnection.getConnection(); 
                 java.sql.PreparedStatement stmt = conn.prepareStatement(query)) {
                 
                 stmt.setInt(1, accId);
                 java.sql.ResultSet rs = stmt.executeQuery();
                 
                 if (rs.next()) {
                     // SUCCESS! Save details to background variables
                     verifiedAccountId = accId;
                     currentAccountBalance = rs.getDouble("balance");
                     currentAccountOwner = rs.getString("first_name") + " " + rs.getString("last_name");
                     
                     // Format the text field: Make text BIGGER and Centered
                     txtCurrentBalance.setFont(new java.awt.Font("SansSerif", java.awt.Font.BOLD, 24));
                     txtCurrentBalance.setHorizontalAlignment(javax.swing.JTextField.CENTER);
                     
                     // Display the formatted string
                     txtCurrentBalance.setText("Owner: " + currentAccountOwner + "   |   Balance: ₱" + currentAccountBalance);
                     
                     javax.swing.JOptionPane.showMessageDialog(this, "Account Verified Successfully!");
                 } else {
                     // FAILED! Reset everything
                     verifiedAccountId = -1;
                     currentAccountBalance = 0.0;
                     currentAccountOwner = "";
                     txtCurrentBalance.setText("");
                     javax.swing.JOptionPane.showMessageDialog(this, "Account not found!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                 }
            }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Account ID must be a valid number.", "Invalid Input", javax.swing.JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_VerificationActionPerformed

